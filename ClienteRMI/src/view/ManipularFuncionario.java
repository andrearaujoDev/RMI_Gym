    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Registry conexao;
        try {
            conexao = LocateRegistry.getRegistry(ip,1100);
            InterfaceFuncionario funcionarioServico =(InterfaceFuncionario) conexao.lookup("funcionario");
            Funcionario f = new Funcionario();
            f.setNome(txtNome.getText());
            f.setSexo(txtSexo.getText());
            f.setData_nasc(txtNascimento.getText());
            f.setSalario(Float.parseFloat(txtSalario.getText()));
            f.setTurno(txtTurno.getText());
            f.setFuncao(txtFuncao.getText());
            f.setTelefone(txtTelefone.getText());
            f.setEmail(txtEmail.getText());
            JOptionPane.showMessageDialog(this, funcionarioServico.inserirFuncionario(f));
            limparCampos();

        }catch (RemoteException erro){
            System.out.println("Erro de conexao com o servidor" + erro.getMessage());
        }catch(NotBoundException erro){
            System.out.println("Erro de conexao com o servidor" + erro.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

