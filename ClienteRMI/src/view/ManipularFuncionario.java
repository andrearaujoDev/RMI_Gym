    private void txtIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIDActionPerformed
        if(!txtID.getText().equals("")){
            Registry conexao;
            try{
                conexao = LocateRegistry.getRegistry(ip,1100);
                InterfaceFuncionario funcionarioServico =(InterfaceFuncionario) conexao.lookup("funcionario");
                int digitado = Integer.parseInt(txtID.getText());
                for(Funcionario f: funcionarioServico.selecionarFuncionario(Integer.parseInt(txtID.getText()))){
                    if(digitado == f.getId_fun()){
                        txtNome.setText(f.getNome());
                        txtSexo.setText(f.getSexo());
                        txtNascimento.setText(f.getData_nasc());
                        txtTurno.setText(f.getTurno());
                        txtFuncao.setText(f.getFuncao());
                        txtTelefone.setText(f.getTelefone());
                        txtEmail.setText(f.getEmail());
                        txtSalario.setText(String.valueOf(f.getSalario()));
                    }
                }
            }catch (RemoteException erro){
                System.out.println("Erro de conexao com o servidor : " + erro.getMessage());
            }catch(NotBoundException erro){
                System.out.println("Erro de conexao com o servidor : " + erro.getMessage());
            }
        }
    }//GEN-LAST:event_txtIDActionPerformed

